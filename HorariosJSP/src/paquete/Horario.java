package paquete;

public class Horario {
	private int clave_horario;
	private String clave_materia;
	private int idprofesor;
	private String grupo;
	private int no_alum;
	private String salon;
	private String lunes;
	private String martes;
	private String miercoles;
	private String jueves;
	private String viernes;
	
	public int getClave_horario() {
		return clave_horario;
	}
	public void setClave_horario(int clave_horario) {
		this.clave_horario = clave_horario;
	}
	public String getClave_materia() {
		return clave_materia;
	}
	public void setClave_materia(String clave_materia) {
		this.clave_materia = clave_materia;
	}
	public int getIdprofesor() {
		return idprofesor;
	}
	public void setIdprofesor(int idprofesor) {
		this.idprofesor = idprofesor;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public int getNo_alum() {
		return no_alum;
	}
	public void setNo_alum(int no_alum) {
		this.no_alum = no_alum;
	}
	public String getSalon() {
		return salon;
	}
	public void setSalon(String salon) {
		this.salon = salon;
	}
	public String getLunes() {
		return lunes;
	}
	public void setLunes(String lunes) {
		this.lunes = lunes;
	}
	public String getMartes() {
		return martes;
	}
	public void setMartes(String martes) {
		this.martes = martes;
	}
	public String getMiercoles() {
		return miercoles;
	}
	public void setMiercoles(String miercoles) {
		this.miercoles = miercoles;
	}
	public String getJueves() {
		return jueves;
	}
	public void setJueves(String jueves) {
		this.jueves = jueves;
	}
	public String getViernes() {
		return viernes;
	}
	public void setViernes(String viernes) {
		this.viernes = viernes;
	}
	
	
	
}
